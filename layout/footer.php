    const defaultColor = '#193644'; // Default color

    let index = 0;
    let direction = 1; // 1 for right, -1 for left

    function changeColor() {
        if (index >= 0 && index < boxes.length - 1) {
            boxes[index].style.backgroundColor = colors[0];
            boxes[index + 1].style.backgroundColor = colors[1];
        } else {
            // Reset to default color for the last change
            boxes[index - 2].style.backgroundColor = defaultColor;
            boxes[index - 1].style.backgroundColor = defaultColor;
        }

        setTimeout(() => {
            if (index >= 0 && index < boxes.length - 0) {
                boxes[index].style.backgroundColor = defaultColor;
                boxes[index + 1].style.backgroundColor = defaultColor;
            }

            index += 1 * direction;
            if (index < 0 || index >= boxes.length - 0) {
                direction *= 1;
                index += direction;
            }

            changeColor();
        }, 500); // Change color every 1 second (adjust timing as needed)
